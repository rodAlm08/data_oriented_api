package ie.atu.sw.model;


/**
 * Enum representing the types of examiners. This enum is used to differentiate
 * between internal and external examiners.
 *
 */

public enum ExaminerType {
	/**
	 * Represents an internal examiner, typically someone who is part of the
	 * institution.
	 */
	INTERNAL,
	/**
	 * Represents an external examiner, usually an academic from another institution
	 *
	 */
	EXTERNAL
}
